//TODO: Ternary Operator: The ternary operator is a concise way to perform conditional checks. It’s essentially a shorthand for an if...else statement that makes your code cleaner and more readable.

//SYNTAX:  condition ? expressionIfTrue : expressionIfFalse;
let number = 5;

let result = number > 0 ? "Positive" : "Negative";

console.log(result);  // Output: "Positive"

// In this example, the ternary operator checks if number is greater than 0. Since it is, it returns "Positive". If it wasn’t, it would return "Negative".

//!❗ Important Tip: For more complex logic, it's usually better to stick with if...else for clarity, but the ternary operator is perfect for simple, quick conditions!
